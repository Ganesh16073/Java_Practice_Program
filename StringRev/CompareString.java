public class CompareString {
    public static void main(String[] args) {
        String str1="Ganesh";
        String str2="Ganesh";
        boolean b=str1.equals(str2); // return boolean if equal
        int a=str1.compareTo(str2); // return 0 if equals
        System.out.println(b+" :: "+a);

    }
    
}
